const arrange_num_ascending =(a, b)=>{
	// a should come before b in the sorted order
    if(a.point < b.point){
            return -1;
    // a should come after b in the sorted order
    }else if(a.point > b.point){
            first_position = a.point;
            second_position = b.point;
            return 1;
    // a and b are the same
    }else{
            return 0;
    }
}
//rearrange the table in descending order
const arrange_num_decending =(a, b)=>{
    if(a.point < b.point){
            return 1;
    }else if(a.point > b.point){
            return -1;
    }else{
            return 0;
    }
}
